    private void loadVehicles(){
        Vehicle v=new Vehicle();
        
        vehicleList=v.getAllVehicles(con.getConnection());
        
        DefaultTableModel dtm=(DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        
        for (Vehicle vobj : vehicleList) {
            String[] rowdata={vobj.getVehicleNo(), vobj.getOwner(), vobj.getType(), vobj.getModel()};
            dtm.addRow(rowdata);
        }
    }
    
    
