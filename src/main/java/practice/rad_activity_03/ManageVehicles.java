    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        selectedVehicle=vehicleList.get(jTable1.getSelectedRow());
        jTextField1.setText(selectedVehicle.getVehicleNo());
        jTextField2.setText(selectedVehicle.getOwner());
        jComboBox1.setSelectedItem(selectedVehicle.getType());
        jTextField3.setText(selectedVehicle.getModel());
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        selectedVehicle.setVehicleNo(jTextField1.getText());
        selectedVehicle.setOwner(jTextField2.getText());
        selectedVehicle.setType(jComboBox1.getSelectedItem().toString());
        selectedVehicle.setModel(jTextField3.getText());
        if(selectedVehicle.updateDetails(con.getConnection())){
            loadVehicles();
        }else{
            JOptionPane.showConfirmDialog(rootPane, "Update Faileure", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

