    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        selectedCustomer=customerList.get(jTable1.getSelectedRow());
        jTextField1.setText(selectedCustomer.getName());
        jComboBox1.setSelectedItem(selectedCustomer.getAddress());
        jTextField2.setText(selectedCustomer.getPhone());
        if("male".equalsIgnoreCase(selectedCustomer.getGender())){
            jRadioButton1.setSelected(true);
            jRadioButton2.setSelected(false);
        }else if("female".equalsIgnoreCase(selectedCustomer.getGender())){
            jRadioButton1.setSelected(false);
            jRadioButton2.setSelected(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        selectedCustomer.setName(jTextField1.getText());
        selectedCustomer.setAddress(jComboBox1.getSelectedItem().toString());
        selectedCustomer.setPhone(jTextField2.getText());
        if(jRadioButton1.isSelected()){
            selectedCustomer.setGender("male");
        }else if(jRadioButton2.isSelected()){
            selectedCustomer.setGender("female");
        }
        if(selectedCustomer.updateDetails(con.getConnection())){
            loadCustomer();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Update Faileure", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(selectedCustomer.deleteDetails(con.getConnection())){
            loadCustomer();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Delete failure", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

