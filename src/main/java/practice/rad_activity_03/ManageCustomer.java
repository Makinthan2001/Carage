    private void loadCustomer(){
        customerList =customer.getAllCustomers(con.getConnection());
        DefaultTableModel dtm=(DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        for (Customer cobj : customerList) {
            String[] rowData={cobj.getName(),cobj.getAddress(),cobj.getPhone(),cobj.getGender()};
            dtm.addRow(rowData);
        }
    }
    
    
    
